public class Tom {
  public static void main(String[] args) {
    int numberOfBones = 10;
    if (numberOfBones >= 10) {
      System.out.println("Tom's got a lot of feathers!");
    }
    else if (numberOfBones >= 1) {
      System.out.println("Tom's got some feathers!");
    }
    else {
      System.out.println("Tom's got no feathers!");
    }
  }
}
