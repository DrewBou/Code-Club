class Escape {
  public static void main(String[] args) {
    // print command does not create a new line
    System.out.print("Hello ");
    System.out.println("World!");

    // \n escape sequence prints a new line
    System.out.println("Matthias: Hello, what's your name?\nMax: Hi, I am Max!");

    // \t creates a tab
    System.out.println("This is a \t tab");

    // escape sequences can be used for other special characters
    System.out.println("\"Being the richest man in the cemetery doesn't matter to me. Going to bed at night saying we've done something wonderful, that's what matters to me.\" \n- Steve Jobs");
  }
}
