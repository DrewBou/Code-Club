class Fido {
  public static void main(String[] args) {
    int numberOfBones = 10;
    if (numberOfBones >= 10) {
      System.out.println("Fido's got a lot of bones!");
    }
    else if (numberOfBones >= 1) {
      System.out.println("Fido's got some bones!");
    }
    else {
      System.out.println("Fido's got no bones!");
    }
  }
}
